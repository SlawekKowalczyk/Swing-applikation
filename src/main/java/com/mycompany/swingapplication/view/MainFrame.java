    /**
     * Responsible for creating buttons. Each button represents one a food.
     */
    public void buttonsBuild() {
        mainFrameButtons = new LinkedList<>();
        for (int i = 0; i < food.getFoods().size(); i++) {
            String name = "<html>"
                    + food.getFoods().get(i).getId() + ". "
                    + food.getFoods().get(i).getName() + ",<br/>&nbsp;&nbsp;"
                    + food.getFoods().get(i).getScientificName()
                    + "</html>";
            String pathImage = "src/main/resources/images/" + food.getIconList().get(i) + ".png/";
            ImageIcon icon = new ImageIcon(pathImage);

            JButton button = new JButton(name, icon);

            button.setActionCommand(food.getFoods().get(i).toString());
            controller.putButtonFoodMap(button, food.getFoods().get(i));
            button.addActionListener(controller);
            mainFrameButtons.add(button);
            mainPanelWithFoods.add(button);
        }
    }

    /**
     * Removes the food list from the main panel.
     */
    public void removeAllFromPanelWithFoods() {
        mainPanelWithFoods.removeAll();
        mainPanelWithFoods.revalidate();
        mainPanelWithFoods.repaint();
    }

    /**
     * Removes selected food from the main panel.
     *
     * @param b Food to remove.
     */
