    /**
     * Fills the list of all tagges.
     */
    public void setAllTagsList() {
        for (int i = 0; i < food.getAllTagsList().size(); i++) {
            attributsList.addElement(food.getAllTagsList().get(i));
        }
        allTags.setModel(attributsList);
    }

