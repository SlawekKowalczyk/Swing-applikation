    /**
     * The method takes and adds tags.
     *
     * @param attribute Tags to add.
     */
    public void setFoodTags(String attribute) {
        for (int i = 0; i < foodTags.getItemCount(); i++) {
            if (foodTags.getItemAt(i).equals(attribute)) {
                return;
            }
        }
        foodTags.addItem(attribute);
    }

